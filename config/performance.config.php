<?php
/**
 * Nextcloud 后台任务窗口 & 数据保留策略
 *
 * 背景：没有这些配置时，Nextcloud 会在任意时间运行重型后台任务（文件全量扫描、
 * 预览批量生成、活动日志清理……），随机打满白天的 CPU；同时垃圾桶、文件版本、
 * 活动日志会无限增长，让 cron 扫描越来越慢。
 *
 * 挂载路径（docker-compose.yaml 中已配置）：
 *   ./config/performance.config.php:/var/www/html/config/performance.config.php:ro
 */
$CONFIG = [

    // ── 后台任务时间窗口（Nextcloud 26+ 支持）──────────────────────────────
    // 把资源密集型后台任务（文件扫描、预览批量生成等）限制在每天 UTC 02:00 开始的
    // 1 小时窗口内执行，避免白天业务高峰期被后台任务抢占 CPU。
    // 注：GCP 机器在北京时间 = UTC+8，UTC 02:00 = 北京时间 10:00；
    // 若要在深夜跑，改为 16（UTC 16:00 = 北京时间 00:00）。
    'maintenance_window_start' => 16,

    // ── 垃圾桶保留策略 ──────────────────────────────────────────────────────
    // 格式："auto,D" = Nextcloud 自动管理，但最多保留 D 天。
    // 默认 "auto" 会尝试永久保留（空间足够时），导致垃圾桶无限增大，
    // 每次 cron 扫描都要遍历大量已删除文件。
    // 改为 auto,30：最多保留 30 天，到期自动清理。
    'trashbin_retention_obligation' => 'auto, 30',

    // ── 文件版本保留策略 ─────────────────────────────────────────────────────
    // 格式同上。默认 "auto" 会保留大量历史版本（特别是频繁编辑的文件）。
    // 改为 auto,60：最多保留 60 天版本，控制版本文件数量，加快扫描速度。
    'versions_retention_obligation' => 'auto, 60',

    // ── 活动日志保留天数 ─────────────────────────────────────────────────────
    // 默认不过期，活动表（oc_activity）可增长到数百万行，
    // 拖慢 MariaDB 查询和 Nextcloud 后台清理任务。
    // 保留最近 90 天活动记录，超出部分由 cron 定期清除。
    'activity_expire_days' => 90,

    // ── 地区设置（消除管理后台警告）────────────────────────────────────────
    // 不影响性能，但未设置时 Nextcloud 管理页面会持续显示"缺少 phone_region"警告，
    // 并在每次 cron 检查时写入日志，轻微增加日志 I/O。
    'default_phone_region' => 'CN',

    // ── 骨架目录（新用户默认文件）───────────────────────────────────────────
    // 默认会给每个新建用户复制一套示例文件（Welcome.txt 等）。
    // 内网部署不需要，设为空字符串禁用，加快新用户初始化速度。
    'skeletondirectory' => '',

    // ── 分片上传并行度（上传时 CPU / 内存峰值的主要来源之一）────────────────
    // 单用户上传大图时浏览器仍会并行多个分片请求；设为 1 与「预览单线程」思路一致，
    // 进一步压低瞬时 CPU（单文件上传总时长可能略增）。
    'files.chunked_upload.max_parallel_count' => 1,

    // ── Activity 应用：挂载点查询缓存 ───────────────────────────────────────
    // 上传/移动文件时会写活动流；开启后减少对挂载点的重复解析，降低 DB 与 CPU。
    'activity_use_cached_mountpoints' => true,

    // ── 本地文件变更检测（显式写入，避免被误改成 1）──────────────────────────
    // 0：不对外部直接改盘做逐请求检测，上传密集时减少 stat 开销（默认即为 0）。
    'filesystem_check_changes' => 0,

    // ── 内网/自建：减少后台对 apps.nextcloud.com 的更新探测与说明站外链 ───────
    'updatechecker' => false,
    'knowledgebaseenabled' => false,

];
